/**
 * Full context for codegen including derived example request body.
 * @param {object} contract App contract row
 */
export function buildGenerationContext(contract) {
  const ctx = buildCtx(contract);
  const exampleBody = ctx.requestSchema ? exampleFromSchema(ctx.requestSchema) : null;
  return { ...ctx, exampleBody };
}

